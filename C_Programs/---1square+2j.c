/* ternary operation operation */
/* pre processor directive */
#include<stdio.h>

/* ternary operation */
int main()
{
int i,j,k;
printf("\nEnter value of i\n",i);
scanf("%d",&i);
printf("\nEnter value of j\n",j);
scanf("%d",&j);
k=(i--)*(i--)+2*j;
printf("\nthe value of k is=%d",k);
scanf("%d",&k);  

return 0;

}

